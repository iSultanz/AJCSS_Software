public class Lookup {
    private String desc;
    private int code;

    /**
     * @return the desc
     */
    public String getDesc() {
        return desc;
    }


    public void setDesc(String desc) {
        this.desc = desc;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }


    public void setCode(int code) {
        this.code = code;
    }
}